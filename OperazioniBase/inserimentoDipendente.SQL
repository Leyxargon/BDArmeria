CREATE OR REPLACE PROCEDURE inserimentoDipendente (
	CF_DIP IN  VARCHAR2,	-- CODICE PRODOTTO EQUIPAGGIAMENTO
	NOME IN  VARCHAR2,		-- NOME DIPENDENTE
	COGNOME IN  VARCHAR2,	-- COGNOME DIPENDENTE
	DATA_N IN  DATE,	-- D.N. DIPENDENTE
	VIA IN  VARCHAR2,	-- VIA DIPENDENTE
	CAP IN NUMBER,      -- CAP DIPENDENTE
	CITTA IN VARCHAR2,  -- CITTA DIPENDENTE
	NUM_TESS IN VARCHAR2  --NUM. PORTO ARMI DIPENDENTE
	)
	AS
	BEGIN
	INSERT INTO PERSONA (CF,NOME,COGNOME,DATA_NASCITA,VIA,CAP,CITTA)
	VALUES	(CF_DIP, NOME, COGNOME, DATA_N, VIA, CAP, CITTA);
	
	INSERT INTO DIPENDENTE (NUM_TESSERINO,CF_DIP)
	VALUES(NUM_TESS, CF_DIP);
	END;
	/